function funcionMath(num1, num2) {
  console.log("Absoluto:" + Math.abs(num1))
  console.log("Ceil:" + Math.ceil(num2))
  console.log("Floor:" + Math.floor(num2))
  console.log("Round:" + Math.round(num2))
  console.log("Max:" + Math.max(num1, num2))
  console.log("Min:" + Math.min(num1, num2))
  console.log("Random:" + Math.random())
}

funcionMath(-10, 23.76)
funcionMath(13, 16.1)
